export type LetterKey = keyof typeof letters; // "A" | "B" | "C" | ... | " "

export function printWord(word: string) {
	const lines = ["", "", "", "", ""];
	for (let i = 0; i < 5; i++) {
		for (let j = 0; j < word.length; j++) {
			const char = word[j];
			const key = char as LetterKey;
			lines[i] += (letters[key] || letters[" "])[i];
			// Add spacing only if not the last character
			if (j < word.length - 1) {
				lines[i] += "  ";
			}
		}
	}

	console.log(lines.join("\n"));
}

export function generateLines(word: string, hash: boolean = false): string[] {
	const lines = ["", "", "", "", ""];
	for (let i = 0; i < 5; i++) {
		for (let j = 0; j < word.length; j++) {
			const char = word[j];
			const key = char as LetterKey;
			let line = (letters[key] || letters[" "])[i];
			if (hash) {
				line = line.replace("*", "#");
			}
			lines[i] += line;

			// Add spacing only if not the last character
			if (j < word.length - 1) {
				lines[i] += "  ";
			}
		}
	}

	return lines;
}
